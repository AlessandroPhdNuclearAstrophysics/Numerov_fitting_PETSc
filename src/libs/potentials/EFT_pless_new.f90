SUBROUTINE EFT_PLESS_PW_NEW(ILB, R, L, S, J, TZ, V)
  IMPLICIT NONE
  DOUBLE PRECISION, PARAMETER :: HTC = 197.32697D0
  DOUBLE PRECISION, PARAMETER :: PI= 4.D0*DATAN(1.D0)
  DOUBLE PRECISION, PARAMETER :: C200 = -9.8383461199448501D0, RV00 = 2.7176606551400253D0
  
  INTEGER, INTENT(IN) :: L, S, J, TZ, ILB
  DOUBLE PRECISION, INTENT(IN) :: R
  DOUBLE PRECISION, INTENT(OUT):: V(2,2)

  INTEGER :: T

  T = MOD(MOD(L+S, 2) + 1, 2)

  V = 0.D0
  IF ( T.EQ.0 .AND. S.EQ.0 ) THEN
    V(1,1) = C200*(-4*R**2 + 6*RV00**2)/(DEXP(R**2/RV00**2)*PI**1.5*RV00**7)
  ENDIF
  V = V*HTC

  RETURN
END SUBROUTINE EFT_PLESS_PW_NEW